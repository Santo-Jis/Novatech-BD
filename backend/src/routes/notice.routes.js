const express = require('express');
const router = express.Router();
const { auth } = require('../middlewares/auth');
const { allowRoles } = require('../middlewares/roleCheck');
const { createNotice, getNotices, getAllNotices, deleteNotice } = require('../controllers/notice.controller');
router.get('/', auth, getNotices);
router.get('/all', auth, allowRoles('admin','manager','supervisor','asm','rsm'), getAllNotices);
router.post('/', auth, allowRoles('admin','manager','supervisor','asm','rsm'), createNotice);
router.delete('/:id', auth, allowRoles('admin','manager','supervisor','asm','rsm'), deleteNotice);
module.exports = router;
